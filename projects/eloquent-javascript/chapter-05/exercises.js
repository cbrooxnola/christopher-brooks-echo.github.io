function flatten(array) {
  let newArray = array.reduce(function(acc, current){
    for (let i = 0; i < current.length; i++){
      acc.push(current[i]);
    }
    return acc;
  }, []);
  return newArray;
}

// /////////////////////////////////////////////////////////////////////////////
// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(first, test, update, body) {
  for (let value = first; test(value); value = update(value)) {
    body(value);
  }
}

