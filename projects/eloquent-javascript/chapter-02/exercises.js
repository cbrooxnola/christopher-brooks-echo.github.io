function fizzBuzz(num1, num2) {
  for (let i = num1; i <= num2; i++){
    if ((i % 3 === 0) && (i % 5 === 0)){
      console.log("fizzbuzz");
    } else if (i % 3 === 0){
      console.log("fizz");
    } else if (i % 5 === 0){
      console.log("buzz");
    } else {
      console.log(i);
    }
  }
}

