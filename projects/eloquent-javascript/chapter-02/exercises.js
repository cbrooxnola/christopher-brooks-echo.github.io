function drawChessboard(x) {
  let board = [];//create array to store rows of strings
  for (let i = 0; i < x; i++){//create loop to run x number of times
    let row = '';
    for (let j = 0; j < x; j++){
      if ((j + i) % 2 === 0){
        row += ' ';
      } else {
        row += '#';
      }
    }
    board.push(row);
 }
 console.log(board.join('\n') + '\n');//return array converted to strings
}

