// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
    let output = [];
    if (start === end){
      return output;
    } 
    if (step === undefined){
          step = 1;
    }
    if (step >= 1) {
      for (let i = start; i <= end; i += step){
        output.push(i);
      }
    } else {
      for (let i = start; i >= end; i += step){
        output.push(i);
      }
    }
    return output;
    }


